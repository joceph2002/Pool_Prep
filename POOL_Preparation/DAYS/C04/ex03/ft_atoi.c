int ft_atoi(char *str)
{
    int	i;
    int r;
    int s;

    i = 0;
    r = 0;
    s = 1;
    while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
        i++;
    while (str[i] == '-' || str[i] == '+')
    {
        if (str[i] == '-')
            s = -s;
        i++;
    }
    while (str[i] >= '0' && str[i] <= '9')
    {
        r = r * 10 + (str[i] - '0');
        i++;
    }
    return (r * s);
}
